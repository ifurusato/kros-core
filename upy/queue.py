class Empty(Exception):
    '''
    An exception thrown when an action is taken upon an empty queue.
    '''
    pass

# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
class Full(Exception):
    '''
    An exception thrown when the queue would overflow upon fulfilling an action.
    '''
    pass

